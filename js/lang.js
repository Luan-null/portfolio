let idiomaAtual = "pt";

function trocarIdioma() {
    // Alterna a variável de idioma
    idiomaAtual = idiomaAtual === "pt" ? "en" : "pt";
    
    // Atualiza o texto do botão (Se tá em PT, mostra opção EN e vice-versa)
    document.getElementById("btn-idioma").innerText = idiomaAtual === "pt" ? "EN" : "PT";

    // Busca todos os elementos do HTML que tem o atributo data-i18n
    const elementos = document.querySelectorAll("[data-i18n]");
    
    // Varre cada elemento e substitui pelo texto do dicionário
    elementos.forEach(elemento => {
        const chave = elemento.getAttribute("data-i18n");
        if (traducoes[idiomaAtual][chave]) {
            // Usa innerHTML para não quebrar a tag <span> do título
            elemento.innerHTML = traducoes[idiomaAtual][chave];
        }
    });

    // Recalcula o orçamento para atualizar a moeda imediatamente
    if (typeof calcularOrcamento === 'function') {
        calcularOrcamento();
    }
}

// Escuta o clique no botão
document.getElementById("btn-idioma").addEventListener("click", trocarIdioma);